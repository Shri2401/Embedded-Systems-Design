/**
  * @brief  Rx Transfer completed callbacks.
  * @param  huart  Pointer to a UART_HandleTypeDef structure that contains
  *                the configuration information for the specified UART module.
  * @retval None
  */
void HAL_UART_RxCpltCallback(UART_HandleTypeDef* huart){

	switch(rxData[0]){

	case 'A'://When user enters the character A, the PWN duty cycle is incremented by 25%.
	{
		if (PWM <= (float)0.96){
					PWM = (PWM + 0.25); //increments the pwm by 5%
					__HAL_TIM_SET_COMPARE(&htim4, TIM_CHANNEL_1,  511*PWM); //compares pwm with the timer channel 1
				    HAL_UART_Transmit(&huart2, txData_inc25, sizeof(txData_inc25), 100);
				}
				else{
					HAL_UART_Transmit(&huart2, txData_high, sizeof(txData_high), 100); //transmits when pwm reaches max duty cycle.
				}
		break;

	}
	case 'B'://When user enters the character B, the PWN duty cycle is decremented by 25%.
	{
		if (PWM >= (float)0.04){
					PWM = (PWM - 0.25); //decrements the pwm by 5%
					__HAL_TIM_SET_COMPARE(&htim4, TIM_CHANNEL_1,  511*PWM); //compares pwm with the timer channel 1
				    HAL_UART_Transmit(&huart2, txData_dec25, sizeof(txData_dec25), 100);
				}
				else{
					HAL_UART_Transmit(&huart2, txData_low, sizeof(txData_low), 100); //transmits when pwm reaches minimum duty cycle.
				}
		break;
	}
	}
}


